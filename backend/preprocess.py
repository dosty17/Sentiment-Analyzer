def preprocess(textdata):
    processedText = []
    wordLemm = WordNetLemmatizer()

    urlPattern        = re.compile(r"((http://)[^ ]*|(https://)[^ ]*|( www\.)[^ ]*)")
    userPattern       = re.compile(r'@[^\s]+')
    sequencePattern   = re.compile(r"(.)\1\1+")
    seqReplacePattern = r"\1\1"

    for tweet in textdata:
        if not isinstance(tweet, str):
            tweet = str(tweet) if tweet is not None else ''
        
        tweet = tweet.lower()
        tweet = urlPattern.sub(' URL', tweet)
        tweet = userPattern.sub(' USER', tweet)

        # Replace emojis with words
        for emoji, meaning in emojis.items():
            tweet = tweet.replace(emoji, f"EMOJI{meaning}")

        # Remove repeating characters (like "coooool" -> "cool")
        tweet = sequencePattern.sub(seqReplacePattern, tweet)

        # âœ… Remove all special characters (except space)
        tweet = re.sub(r'[^a-zA-Z0-9\s]', '', tweet)

        # Normalize spacing
        tweet = re.sub(r'\s+', ' ', tweet).strip()

        # Lemmatize and remove stopwords
        tweetwords = ' '.join([
            wordLemm.lemmatize(word)
            for word in tweet.split()
            if len(word) > 1 and word not in stopwordlist
        ])

        processedText.append(tweetwords)

    return processedText
